package exercises.exercisesP1.E3;

public class Cuaderno {
    private String tipo;
    private int nroDeHojas;

    public Cuaderno(){}

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getNroDeHojas() {
        return nroDeHojas;
    }

    public void setNroDeHojas(int nroDeHojas) {
        this.nroDeHojas = nroDeHojas;
    }
}
