package exercises.exercisesP1.E3;

public class Libro {
    private String genero;
    private String autor;

    public Libro(){}

    public String getGenero() {
        return genero;
    }

    public void setGenero(String genero) {
        this.genero = genero;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }
}
